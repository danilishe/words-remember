    const wordsInput = document.getElementById('words-input');
    const submitWordsButton = document.getElementById('submit-words');
    const resultContainer = document.getElementById('result');
    const themeToggleButton = document.getElementById('theme-toggle');

    let selectedWords = [];
    let currentWordIndex = 0;
    let numCards = 10;

    buttonsContainer.addEventListener('click', (event) => {
        if (event.target.classList.contains('btn')) {
            const group = event.target.dataset.group;
            showWords(group);
        }
    });

    function showWords(group) {
        let wordsArray;
        switch (group) {
            case '1':
                wordsArray = group1;
                break;
            case '2':
                wordsArray = group2;
                break;
            case '3':
                wordsArray = group3;
                break;
            case '4':
                wordsArray = group1.map(word => `${word} ${adjectives4[Math.floor(Math.random() * adjectives4.length)]}`);
                break;
            case '5':
                wordsArray = group1.map(word => `${word} ${adjectives5[Math.floor(Math.random() * adjectives5.length)]}`);
                break;
        }
        selectedWords = [];
        for (let i = 0; i < numCards; i++) {
            let randomWord = wordsArray[Math.floor(Math.random() * wordsArray.length)];
            selectedWords.push(randomWord);
        }
        currentWordIndex = 0;
        displayNextWord();
    }

    function displayNextWord() {
        if (currentWordIndex < selectedWords.length) {
            wordToRemember.textContent = selectedWords[currentWordIndex];
            currentWordIndex++;
            wordModal.show();
        } else {
            wordModal.hide();
            inputModal.show();
        }
    }

    nextWordButton.addEventListener('click', displayNextWord);

    submitWordsButton.addEventListener('click', () => {
        inputModal.hide();
        let userWords = wordsInput.value.split('\n').map(word => word.trim().toLowerCase());
        let correctCount = 0;
        const result = selectedWords.map((word, index) => {
            if (userWords[index] === word.toLowerCase()) {
                correctCount++;
                return `✅ ` + word;
            } else {
                return `❌ ${userWords[index] || 'нет ответа'} (${word})`;
            }
        });
        resultContainer.innerHTML = `<p>Вы правильно вспомнили ${correctCount} из ${selectedWords.length} слов.</p>
       ${result.map(word => `<p>${word}</p>`).join('\n')}`;
    });

    themeToggleButton.addEventListener('click', () => {
        document.body.classList.toggle('dark-mode');
    });
